// One-off generator for lib/leetcodeLegacyMap.ts.
//
// It captures a FROZEN snapshot of the legacy (order-based) storage keys
// mapped to the new order-independent keys, based on the CURRENT ordering of
// RAW_PATTERNS. Run this ONCE, before ever reordering patterns/problems:
//
//   node scripts/gen-legacy-map.cjs
//
// After it is written, treat lib/leetcodeLegacyMap.ts as immutable history --
// regenerating it after a reorder would corrupt the migration for any user who
// has not migrated yet.

const fs = require("fs");
const path = require("path");
const ts = require("typescript");
const Module = require("module");

const dataPath = path.join(__dirname, "..", "lib", "leetcodeData.ts");
const source = fs.readFileSync(dataPath, "utf8");

const { outputText } = ts.transpileModule(source, {
  compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2019 }
});

// Evaluate the transpiled (dependency-free) module in isolation.
const m = new Module(dataPath);
m._compile(outputText, dataPath);
const { LEETCODE_PATTERNS } = m.exports;

const patternIdToSlug = {};
const problemKeyToKey = {};

LEETCODE_PATTERNS.forEach((pattern, patternIndex) => {
  const oldId = patternIndex + 1;
  patternIdToSlug[String(oldId)] = pattern.slug;
  pattern.problems.forEach((problem, problemIndex) => {
    problemKeyToKey[`${oldId}:${problemIndex}`] = problem.key;
  });
});

const banner = `// AUTO-GENERATED by scripts/gen-legacy-map.cjs -- DO NOT EDIT OR REGENERATE.
//
// Frozen snapshot mapping legacy order-based storage keys to the current
// order-independent keys. Used one time per user to migrate solved/attempt/
// note data off the fragile \`\${id}:\${index}\` / \`\${id}\` scheme. Regenerating
// this after any pattern/problem reorder would corrupt un-migrated users' data.
`;

const fileBody =
  banner +
  "\n" +
  "export const LEGACY_PATTERN_ID_TO_SLUG: Record<string, string> = " +
  JSON.stringify(patternIdToSlug, null, 2) +
  ";\n\n" +
  "export const LEGACY_PROBLEM_KEY_TO_KEY: Record<string, string> = " +
  JSON.stringify(problemKeyToKey, null, 2) +
  ";\n";

const outPath = path.join(__dirname, "..", "lib", "leetcodeLegacyMap.ts");
fs.writeFileSync(outPath, fileBody);

console.log(
  `Wrote ${outPath}: ${Object.keys(patternIdToSlug).length} patterns, ${
    Object.keys(problemKeyToKey).length
  } problems.`
);
